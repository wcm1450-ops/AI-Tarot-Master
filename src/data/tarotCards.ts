export const ALL_CARDS: TarotCard[] = [...MAJOR_ARCANA, ...MINOR_ARCANA];

export function getCardById(id: number): TarotCard | undefined {
  return ALL_CARDS.find(card => card.id === id);
}

export function drawRandomCards(count: number): { card: TarotCard; isReversed: boolean }[] {
  const shuffled = [...ALL_CARDS].sort(() => Math.random() - 0.5);
  const result = [];
  for (let i = 0; i < count; i++) {
    result.push({
      card: shuffled[i],
      isReversed: Math.random() > 0.7  // 30% conversion rate of being reversed
    });
  }
  return result;
}
